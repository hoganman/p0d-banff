# T2K Data and MC POT


class T2KPOT(object):
    """a namespace for POT"""

    FHC_WTR_DATAPOT = 2.241e+20  # includes run1
    FHC_AIR_DATAPOT = 3.723e+20
    RHC_WTR_DATAPOT = 2.852e+20
    RHC_AIR_DATAPOT = 3.382e+20
    RUN2_WTR_MCPOT = 1.203412e+21
    RUN2_AIR_MCPOT = 9.239371e+20
    RUN3B_AIR_MCPOT = 4.478639e+20
    RUN3C_AIR_MCPOT = 2.632266e+21
    RUN4_AIR_MCPOT = 3.499604e+21
    RUN4_WTR_MCPOT = 3.497117e+21
    RUN5C_WTR_MCPOT = 2.277663e+21
    RUN6B_AIR_MCPOT = 1.417404e+21
    RUN6C_AIR_MCPOT = 5.275622e+20
    RUN6D_AIR_MCPOT = 6.884002e+20
    RUN6E_AIR_MCPOT = 8.594394e+20
    RUN7B_WTR_MCPOT = 3.370465e+21
    FHC_WTR_MCPOT = RUN2_WTR_MCPOT + RUN4_WTR_MCPOT
    FHC_AIR_MCPOT = RUN2_AIR_MCPOT + RUN3B_AIR_MCPOT \
        + RUN3C_AIR_MCPOT + RUN4_AIR_MCPOT
    RHC_WTR_MCPOT = RUN5C_WTR_MCPOT + RUN7B_WTR_MCPOT
    RHC_AIR_MCPOT = RUN6B_AIR_MCPOT + RUN6C_AIR_MCPOT \
        + RUN6D_AIR_MCPOT + RUN6E_AIR_MCPOT

    WTR_DATAPOT = FHC_WTR_DATAPOT + RHC_WTR_DATAPOT
    AIR_DATAPOT = FHC_AIR_DATAPOT + RHC_AIR_DATAPOT

    WTR_MCPOT = FHC_WTR_MCPOT + RHC_WTR_MCPOT
    AIR_MCPOT = FHC_AIR_MCPOT + RHC_AIR_MCPOT

    DATAPOT = WTR_DATAPOT + AIR_DATAPOT
    MCPOT = WTR_MCPOT + AIR_MCPOT

    def __init__(self):
        self.name = 'T2KPOT'

    def __str__(self):
        return self.name
